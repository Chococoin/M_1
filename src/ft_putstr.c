#include "libftprintf.h"

int	ft_putstr(char *c)
{
	int	len;

	len = 0;
	if (!c)
		return (write(1, "(null)", 6));
	while (*c)
	{
		write(1, c, 1);
		c++;
		len++;
	}
	return (len);
}
